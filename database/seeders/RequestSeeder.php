    private function pickUnusedUser($users, array &$usedUserIds)
    {
        foreach ($users->shuffle()->values() as $user) {
            if (! in_array($user->id, $usedUserIds, true)) {
                $usedUserIds[] = $user->id;

                return $user;
            }
        }

        return null;
    }
